#include <iostream>
using namespace std;

int main(){
    int x = 10;
    cout << "sizeof(int) = " << sizeof(int) << endl;
    cout << "sizeof x = " << sizeof x << endl;
    cout << "sizeof(short) = " << sizeof(short) << endl;
    cout << "sizeof(char) = " << sizeof(char) << endl;
    cout << "sizeof(float) = " << sizeof(float) << endl;
    cout << "sizeof(double) = " << sizeof(double) << endl;
}